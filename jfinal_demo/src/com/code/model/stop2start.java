package com.code.model;

public class stop2start {

	private String token;
	private String[] devSn;
	private String rxSpeed;
	private String txSpeed;
	private String webLimit;
	private String appLimit;
	
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String[] getDevSn() {
		return devSn;
	}
	public void setDevSn(String[] devSn) {
		this.devSn = devSn;
	}
	public String getRxSpeed() {
		return rxSpeed;
	}
	public void setRxSpeed(String rxSpeed) {
		this.rxSpeed = rxSpeed;
	}
	public String getTxSpeed() {
		return txSpeed;
	}
	public void setTxSpeed(String txSpeed) {
		this.txSpeed = txSpeed;
	}
	public String getWebLimit() {
		return webLimit;
	}
	public void setWebLimit(String webLimit) {
		this.webLimit = webLimit;
	}
	public String getAppLimit() {
		return appLimit;
	}
	public void setAppLimit(String appLimit) {
		this.appLimit = appLimit;
	}

	
}
